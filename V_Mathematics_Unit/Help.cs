        public static VRandom GetRNG(int index, uint salt)
        {
            uint seed = seeds[index] ^ salt;
            int sseed = unchecked((int)seed);
            return new RandR30(sseed);
        }

        public static Cmplx RandCmplx(this VRandom rng, double theta)
        {
            double a = rng.RandGauss(0.0, theta);
            double b = rng.RandGauss(0.0, theta);

            return new Cmplx(a, b);
        }
